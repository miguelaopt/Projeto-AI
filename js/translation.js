// funcao que troca os textos todos
function changeLanguage(lang) {
    localStorage.setItem('royal_lang', lang);

    const elements = document.querySelectorAll('[data-lang]');
    
    elements.forEach(el => {
        const key = el.getAttribute('data-lang');
        if (window.translations[lang] && window.translations[lang][key]) {
            const translation = window.translations[lang][key];
            
            // se for input muda o placeholder senao muda o texto normal
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.placeholder = translation;
            } else {
                if(translation.includes("<")) {
                    el.innerHTML = translation;
                } else {
                    el.innerText = translation;
                }
            }
        }
    });

    // poe o botao da lingua ativo (bold)
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.remove('active');
        if(btn.dataset.btnLang === lang) btn.classList.add('active'); 
    });

    // avisa o resto do site (tipo o react) que mudou
    window.dispatchEvent(new Event('languageChange'));
}

// corre logo ao inicio para nao ficar em pt se o user escolheu en
document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('royal_lang') || 'pt';
    changeLanguage(savedLang);
});